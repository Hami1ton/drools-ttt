package org.example.drools.ttt.model;

public class Mark {
    
    private int row;
    
    private int col;

    private String mark;

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }

    public String getMark() {
        return mark;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public void setCol(int col) {
        this.col = col;
    }

    public void setMark(String mark) {
        this.mark = mark;
    }
}
